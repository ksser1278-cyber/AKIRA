def night_drive_profile_section_bank(profile: str, hook: str) -> dict[str, list[str]]:
    section_bank = NIGHT_DRIVE_PROFILE_SECTION_BANKS.get(profile, {})
    if not section_bank:
        return {}
    resolved = {section: list(lines) for section, lines in section_bank.items()}
    if "chorus_open" in resolved:
        resolved["chorus_open"] = [hook, *resolved["chorus_open"]]
    if "chorus" in resolved:
        resolved["chorus"] = [hook, *resolved["chorus"]]
    if "chorus_final" in resolved:
        resolved["chorus_final"] = [hook, *resolved["chorus_final"]]
    return resolved
